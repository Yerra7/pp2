# String Concatenation
''' 
String concatenation is the process of combining two or more strings into one.
You can concatenate strings using the + operator.
'''

# Example 1: Merge variable a with variable b into variable c:
a = "Hello"
b = "World"
c = a + b  # Concatenates 'a' and 'b' directly
print(c)  # Output: HelloWorld

# Example 2: To add a space between the strings, use " " between them:
a = "Hello"
b = "World"
c = a + " " + b  # Adds a space between 'a' and 'b'
print(c)  # Output: Hello World
